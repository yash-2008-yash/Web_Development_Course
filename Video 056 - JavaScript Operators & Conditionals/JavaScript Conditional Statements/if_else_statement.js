if (10 < 5) {
    console.log("Yes, 10 is lesser than 5")
}

else {
    console.log("No, 10 is not lesser than 5")
}

// Here, the 'if' block of code will run if the condition is true.
// If it's false, the 'else' block of code will run.

// This can be written in a more convinient way using ?:

(10 < 5) ? console.log("Yes, 10 is lesser than 5") : console.log("No, 10 is not lesser than 5");

// This is the ternary operator (?:)

// Syntax: condition ? expression_for_true : expression_for_false ;