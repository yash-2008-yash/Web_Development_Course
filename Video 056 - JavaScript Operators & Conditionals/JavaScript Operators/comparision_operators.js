// Comparision Operations
// Compares and returns true and false

let number1 = 10
let number2 = 2

console.log(number1 == number2)
// checks whether number1 is equal to number2
// if yes, returns 'true', and if not, returns 'false'
// in this case, 10 and 2 aren't equal, so it returns 'false'

console.log(number1 != number2)
// checks whether number1 is not equal to number2
// if yes, returns 'true', and if not, returns 'false'
// in this case, 10 and 2 aren't equal, so it returns 'true'

console.log(number1 === number2)
// checks whether number1 is equal to number2 (checks values and data types)
// if values and types are same, returns 'true', and if not, returns 'false'
// in this case, 10 and 2 aren't equal but same data type, so it returns 'false'

console.log(number1 !== number2)
// checks whether number1 is not equal to number2 (checks values and data types)
// if values and types are same, returns 'false', and if not, returns 'true'
// in this case, 10 and 2 aren't equal but same data type, so it returns 'true'

console.log(number1 > number2)
// checks whether number1 is greater than number2
// if yes, returns 'true', and if not, returns 'false'
// in this case, 10 is greater than 2, so it returns 'true'

console.log(number1 < number2)
// checks whether number1 is lesser than number2
// if yes, returns 'true', and if not, returns 'false'
// in this case, 10 is not lesser than 2, so it returns 'false'

console.log(number1 >= number2)
// checks whether number1 is greater than or equal to number2
// if yes, returns 'true', and if not, returns 'false'
// in this case, 10 is greater than 2, so it returns 'true'

console.log(number1 <= number2)
// checks whether number1 is lesser than or equal to number2
// if yes, returns 'true', and if not, returns 'false'
// in this case, 10 is not lesser than 2, so it returns 'false'