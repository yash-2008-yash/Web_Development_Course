// Arithmetic Operations
// Performs basic arithmetic operations

let number1 = 10
let number2 = 2

console.log(`Sum of ${number1} and ${number2} is ${number1 + number2}`)
// adds 10 and 2 (12)

console.log(`Difference of ${number1} and ${number2} is ${number1 - number2}`)
// subtracts 2 from 10 (8)

console.log(`Product of ${number1} and ${number2} is ${number1 * number2}`)
// multiplies 10 and 2 (20)

console.log(`Quotient of ${number1} and ${number2} is ${number1 / number2}`)
// divides 10 by 2 and returns the quotient (5)

console.log(`Remainder of ${number1} and ${number2} is ${number1 % number2}`)
// divides 10 by 2 and returns the remainder (0)

console.log(`${number1} to the power ${number2} is ${number1 ** number2}`)
// 2 becomes exponent of 10 (10^2=100)

number1++ // This increases the value of number1 by 1
console.log(number1) // 10 + 1 = 11

number2-- // This decreases the value of number2 by 1
console.log(number2) // 2 - 1 = 1